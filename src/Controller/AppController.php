namespace App\Controller;

use Cake\Controller\Controller;

/**
 * Application Controller
 *
 * Add your application-wide methods in the class below, your controllers
 * will inherit them.
 *
 * @link https://book.cakephp.org/4/en/controllers.html#the-app-controller
 */
class AppController extends Controller
{
    /**
     * Initialization hook method.
     *
     * Use this method to add common initialization code like loading components.
     *
     * e.g. `$this->loadComponent('FormProtection');`
     *
     * @return void
     */
    public function initialize(): void
    {
        $this->loadComponent('Flash');
        $this->loadComponent('Auth', [
            'authorize'=> 'Controller',
            'authenticate' => [
                'Form' => [
                    'fields' => [
                        'username' => 'email',
                        'password' => 'password'
                    ]
                ]
            ],
            'loginAction' => [
                'controller' => 'Users',
                'action' => 'login'
            ],
            'unauthorizedRedirect' => $this->referer() // If unauthorized, return them to page they were just on
        ]);
    
        // Permitir acceso a la acción login del controlador Users
        $this->Auth->allow(['display', 'login']);
    
        // Restringir el acceso a todas las demás acciones del controlador Users
        $this->Auth->deny(['add', 'edit', 'delete']);
    }
    

    //=========================================Restringir BOOKMARKS==================================
    public function isAuthorized($user)
    {
        // Esta función se utiliza para determinar si el usuario actual tiene autorización para realizar acciones específicas.
        // Al devolver siempre false, se deniega automáticamente el acceso a todas las acciones para todos los usuarios.
        // Esto significa que ningún usuario tendrá permiso para realizar ninguna acción en este controlador.
        return false;
    }
}
